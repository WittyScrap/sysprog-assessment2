/**
 * Set video plane.
 *
 * On entry, plane must be set to a value between 0 and 3.
 */
void consolevgaplane(uchar plane) {
    uchar planeMask;

    plane &= 3;
    planeMask = 1 << plane;
    // Set read plane
    outb(VGA_GC_INDEX, 4);
    outb(VGA_GC_DATA, plane);
    // Set write plane
    outb(VGA_SEQ_INDEX, 2);
    outb(VGA_SEQ_DATA, planeMask);
}

// Write the specified values to the VGA registers
static void writeVideoRegisters(uchar* regs) {
    unsigned i;

    /* write MISCELLANEOUS reg */
    outb(VGA_MISC_WRITE, *regs);
    regs++;
    /* write SEQUENCER regs */
    for (i = 0; i < VGA_NUM_SEQ_REGS; i++)
    {
        outb(VGA_SEQ_INDEX, i);
        outb(VGA_SEQ_DATA, *regs);
        regs++;
    }
    /* Unlock CRTC registers */
    outb(VGA_CRTC_INDEX, 0x03);
    outb(VGA_CRTC_DATA, inb(VGA_CRTC_DATA) | 0x80);
    outb(VGA_CRTC_INDEX, 0x11);
    outb(VGA_CRTC_DATA, inb(VGA_CRTC_DATA) & ~0x80);
    /* Make sure they remain unlocked */
    regs[0x03] |= 0x80;
    regs[0x11] &= ~0x80;
    /* write CRTC regs */
    for (i = 0; i < VGA_NUM_CRTC_REGS; i++)
    {
        outb(VGA_CRTC_INDEX, i);
        outb(VGA_CRTC_DATA, *regs);
        regs++;
    }
    /* write GRAPHICS CONTROLLER regs */
    for (i = 0; i < VGA_NUM_GC_REGS; i++)
    {
        outb(VGA_GC_INDEX, i);
        outb(VGA_GC_DATA, *regs);
        regs++;
    }
    /* write ATTRIBUTE CONTROLLER regs */
    for (i = 0; i < VGA_NUM_AC_REGS; i++)
    {
        (void)inb(VGA_INSTAT_READ);
        outb(VGA_AC_INDEX, i);
        outb(VGA_AC_WRITE, *regs);
        regs++;
    }
    /* lock 16-color palette and unblank display */
    (void)inb(VGA_INSTAT_READ);
    outb(VGA_AC_INDEX, 0x20);
}

void writeFont(uchar * fontBuffer, unsigned int fontHeight) {
    uchar		 seq2, seq4, gc4, gc5, gc6;
    unsigned int i;
    uchar*	 fontBase;

    // Save VGA registers
    outb(VGA_SEQ_INDEX, 2);
    seq2 = inb(VGA_SEQ_DATA);

    outb(VGA_SEQ_INDEX, 4);
    seq4 = inb(VGA_SEQ_DATA);
    outb(VGA_SEQ_DATA, seq4 | 0x04);

    outb(VGA_GC_INDEX, 4);
    gc4 = inb(VGA_GC_DATA);

    outb(VGA_GC_INDEX, 5);
    gc5 = inb(VGA_GC_DATA);
    outb(VGA_GC_DATA, gc5 & ~0x10);

    outb(VGA_GC_INDEX, 6);
    gc6 = inb(VGA_GC_DATA);
    outb(VGA_GC_DATA, gc6 & ~0x02);

    consolevgaplane(2);
    // Write font to video memory
    fontBase = (uchar*)P2V(0xB8000);
    for (i = 0; i < 256; i++)
    {
        memmove((ushort*)fontBase, fontBuffer, fontHeight);
        fontBase += 32;
        fontBuffer += fontHeight;
    }
    // Restore VGA registers
    outb(VGA_SEQ_INDEX, 2);
    outb(VGA_SEQ_DATA, seq2);
    outb(VGA_SEQ_INDEX, 4);
    outb(VGA_SEQ_DATA, seq4);
    outb(VGA_GC_INDEX, 4);
    outb(VGA_GC_DATA, gc4);
    outb(VGA_GC_INDEX, 5);
    outb(VGA_GC_DATA, gc5);
    outb(VGA_GC_INDEX, 6);
    outb(VGA_GC_DATA, gc6);
}

/**
 * Video mode switching function which must be called from kernel-space, returning `0` if a valid
 * mode was requested, otherwise `-1`.
 *
 * Currently, only these modes are supported:
 *   0x03: 80x25 text mode.
 *   0x12: 640x480x16 graphics mode.
 *   0x13: 320x200x256 graphics mode.
 */
int consolevgamode(int vgamode) {
    acquire(&cons.lock);

    int errorcode = -1;

    switch (vgamode)
    {
        case 0x03: {
            writeVideoRegisters(registers_80x25_text);
            writeFont(font_8x16, 16);

            currentvgamode = 0x03;
            errorcode = 0;
        } break;

        case 0x12: {
            writeVideoRegisters(registers_640x480x16);

            currentvgamode = 0x12;
            errorcode = 0;
        } break;

        case 0x13: {
            writeVideoRegisters(registers_320x200x256);

            currentvgamode = 0x13;
            errorcode = 0;
        } break;
    }

    release(&cons.lock);

    return errorcode;
}

/**
 * Get the virtual base address of the video memory associated with the current video plane.
 *
 * Note the mapping of CPU memory to video memory is not immediately obvious and, at first sight,
 * this routine looks like it gives an incorrect value for plane 1. However, reading the article at
 * http://www.osdever.net/FreeVGA/vga/vgamem.htm will give you more insight into what is going on.
 *
 * Returns a pointer to the virtual address (NOT the physical address) associated with the current
 * video plane.
 */
uchar* consolevgabuffer() {
    uchar* base;
    uchar plane;

    outb(VGA_GC_INDEX, 6);

    plane = inb(VGA_GC_DATA);
    plane >>= 2;
    plane &= 3;

    switch (plane)
    {
    case 0:
    case 1:
        base = (uchar*)P2V(0xA0000);
        break;

    case 2:
        base = (uchar*)P2V(0xB0000);
        break;

    case 3:
        base = (uchar*)P2V(0xB8000);
        break;
    }

    return base;
}